{
  document.getElementById('add-ingredient').addEventListener('click', function() {
    // 入力された材料を取得
    const ingredientNameInput =document.getElementById('ingredient_name');
    const ingredientValue = ingredientNameInput.value.trim(); // trim関数で空白を削除

    // 材料表示エリアに新しい要素を追加
    if (ingredientValue !== ''){
      const ingredientList = document.getElementById('ingredient-list');
      const newIngredient = document.createElement('div');
      newIngredient.textContent = ingredientValue;
      ingredientList.appendChild('newIngredient'); // 親要素の後に追加

      // 入力フォームをクリア
      ingredientNameInput.value = '';
    }
  });
}
